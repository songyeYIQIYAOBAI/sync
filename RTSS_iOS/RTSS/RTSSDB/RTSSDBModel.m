#import "RTSSDBModel.h"

@implementation RTSSDBModel

+ (NSString*)createConfigTableSql{
    NSString* format = @"CREATE TABLE "CONFIG_TABLE_NAME@" (%@ INTEGER DEFAULT 10)";
    return [NSString stringWithFormat:format, CONFIG_VERSION_COLUMN];
}

+ (NSString*)createFriendsTableSql{
    NSString* format = @"CREATE TABLE "FRIENDS_TABLE_NAME@" (%@ INTEGER PRIMARY KEY AUTOINCREMENT, %@ NVARCHAR, %@ NVARCHAR, %@ NVARCHAR, %@ NVARCHAR, %@ NVARCHAR, %@ INTEGER, UNIQUE (%@,%@))";
    return [NSString stringWithFormat:format, FRIENDS_SN_COLUMN, FRIENDS_MYID_COLUMN, FRIENDS_ID_COLUMN, FRIENDS_NAME_COLUMN, FRIENDS_PHONENUMBER_COLUMN, FRIENDS_PORTRAIT_COLUMN, FRIENDS_TIMESTAMP_COLUMN, FRIENDS_MYID_COLUMN, FRIENDS_PHONENUMBER_COLUMN];
}

+ (NSString*)createEventTableSql{
    NSString* format = @"CREATE TABLE "EVENT_TABLE_NAME@" (%@ INTEGER PRIMARY KEY AUTOINCREMENT, %@ NVARCHAR, %@ INTEGER, %@ NVARCHAR, %@ NVARCHAR, %@ BLOB, %@ INTEGER)";
    return [NSString stringWithFormat:format, EVENT_ID_COLUMN, EVENT_MYID_COLUMN, EVENT_TYPE_COLUMN, EVENT_PEER_PHONENUMBER_COLUMN, EVENT_DESC_COLUMN, EVENT_PARAMETERS_COLUMN, EVENT_TIMESTAMP_COLUMN];
}

+ (NSString*)createMessageTableSql{
    NSString* format = @"CREATE TABLE "MESSAGE_TABLE_NAME@" (%@ NVARCHAR PRIMARY KEY, %@ INTEGER, %@ NVARCHAR, %@ NVARCHAR, %@ NVARCHAR, %@ NVARCHAR, %@ DOUBLE, %@ NVARCHAR, %@ INTEGER)";
    return [NSString stringWithFormat:format, MESSAGE_ID_COLUMN, MESSAGE_TYPE_COLUMN, MESSAGE_FROMCODE_COLUMN, MESSAGE_FROMNAME_COLUMN, MESSAGE_TITLE_COLUMN, MESSAGE_CONTENT_COLUMN, MESSAGE_TIMESTAMP_COLUMN, MESSAGE_HREF_COLUMN, MESSAGE_READ_COLUMN];
}

